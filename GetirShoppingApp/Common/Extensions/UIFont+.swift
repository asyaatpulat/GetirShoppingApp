import UIKit

extension UIFont {

    class func openSansBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "OpenSans-Bold", size: size)!
    }

    class func openSansSemiBold(ofSize size: CGFloat) -> UIFont {
        return UIFont(name: "OpenSans-SemiBold", size: size)!
    }
}
